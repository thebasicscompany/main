// =============================================================================
// Module-level facade. Tests boot in NODE_ENV=test and get the in-memory
// impl by default; production picks Drizzle.
// =============================================================================

let activeRepo: RunStateRepo | null = null

function selectDefaultRepo(): RunStateRepo {
  // Tests rely on a shared in-memory store across `register` and the route
  // layer's `assertWorkspaceMatch`, so use the memory impl whenever
  // NODE_ENV is 'test'.
  if (process.env.NODE_ENV === 'test') return createMemoryRepo()
  return createDrizzleRepo()
}

function getRepo(): RunStateRepo {
  if (!activeRepo) activeRepo = selectDefaultRepo()
  return activeRepo
}

export async function register(record: RunRecord): Promise<void> {
  await getRepo().register(record)
}

export async function update(
  runId: string,
  partial: Partial<Omit<RunRecord, 'runId' | 'workspaceId'>>,
): Promise<RunRecord> {
  return await getRepo().update(runId, partial)
}

export async function get(runId: string): Promise<RunRecord> {
  return await getRepo().get(runId)
}

/**
 * List runs scoped to a workspace, newest first. Used by the audit-query
 * route layer; the workspaceId filter is mandatory and the route layer
 * derives it from the JWT (no cross-workspace queries possible).
 */
export async function list(input: ListRunsInput): Promise<RunRecord[]> {
  return await getRepo().list(input)
}

/**
 * Throw `RunAccessDeniedError` if the requesting workspace does not own
 * the run. Used by the SSE/snapshot routes before exposing run state.
 *
 * Returns the record on success so the caller can avoid a second lookup.
 */
export async function assertWorkspaceMatch(
  runId: string,
  workspaceId: string,
): Promise<RunRecord> {
  const r = await get(runId)
  if (r.workspaceId !== workspaceId) {
    throw new RunAccessDeniedError()
  }
  return r
}

/** Test-only: install a specific repo (e.g. a memory impl) for the duration of a test. */
export function __setRunStateRepoForTests(repo: RunStateRepo | null): void {
  activeRepo = repo
}

/**
 * Test-only: drop in-memory state. If the active repo isn't the memory
 * impl this is a no-op (tests that need to clear DB state must do so
 * themselves).
 */
export function __resetForTests(): void {
  if (activeRepo && '__reset' in activeRepo) {
    ;(activeRepo as { __reset: () => void }).__reset()
  } else {
    activeRepo = null
  }
}
